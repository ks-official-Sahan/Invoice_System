    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String brand = brandName.getText();
        String description = this.description.getText();

        if (brand.equals("")) {

            JOptionPane.showMessageDialog(this, "Enter Brand Name", "Warning", JOptionPane.ERROR_MESSAGE);

        } else if (description.equals("")) {

            JOptionPane.showMessageDialog(this, "Enter Description", "Warning", JOptionPane.ERROR_MESSAGE);

        } else {

            try {

                Date dt = new Date();
                SimpleDateFormat st = new SimpleDateFormat("YYYY-MM-dd hh:mm:ss");
                String tt = st.format(dt);

                MySQL.execute("INSERT INTO `brands` (`name`,`description`,`created_at`)"
                        + "VALUES ('" + brand + "','" + description + "','" + tt + "')");
                
               brandName.setText("");
               this.description.setText("");

            } catch (Exception e) {
                e.printStackTrace();
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed


