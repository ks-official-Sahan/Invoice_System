    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String username = usernameField.getText();
        String email = emailField.getText();
        String mobile = mobileField.getText();
        String role = String.valueOf(this.roleComboBox.getSelectedItem());
        String status = String.valueOf(this.statusComboBox.getSelectedItem());
        String fname = fnameField.getText();
        String lname = lnameField.getText();
        String password = String.valueOf(passwordField.getPassword());

        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter username", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter First Name", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Last Name", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Password", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Entrer your Email", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@"
                + "[^-][A-Za-z0-9\\+-]+(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")) {
            JOptionPane.showMessageDialog(this, "Invalid Email", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter your Mobile", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "Invalid Mobile Number", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (role.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Select Destrict", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (status.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Select City", "Warning", JOptionPane.ERROR_MESSAGE);

        } else {

            try {

                ResultSet resultSet = MySQL.execute("SELECT * FROM `users` WHERE `phone`='" + mobile + "' OR `email`='" + email + "'");

                if (resultSet.next()) {

                    JOptionPane.showMessageDialog(this, "User email or mobile already registered", "Warning", JOptionPane.ERROR_MESSAGE);

                } else {

                    String dateTime = new SQLDateFormatter().getStringDate(new Date(), "YYYY-MM-dd hh:mm:ss");

                    String query = "INSERT INTO "
                            + "`users` (`firstname`, `lastname`, `username`, `email`, `password`, `avatar`, `phone`,"
                            + " `role_id`, `status`, `is_all_warehouses`, `created_at`, `updated_at`, `deleted_at`, `shop_id`) "
                            + "VALUES ('" + fname + "', '" + lname + "', '" + username + "', '" + email + "', '" + password + "', NULL, '" + mobile + "',"
                            + " '" + (role.equalsIgnoreCase("owner") ? "1" : "2") + "', '" + (status.equalsIgnoreCase("active") ? "1" : "2") + "', 0, '" + dateTime + "', NULL, NULL, '"+Application.getShop().getStringId()+"')";

                    MySQL.execute(query);

                }

            } catch (SQLException ex) {
                DatabaseLogger.logger.log(Level.SEVERE, "User Registration error: " + ex.getMessage(), ex.getMessage());
            }

            reset();
            Application.appService.openUserList();
            if (this.modal != null) {
                modal.closeModal();
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

