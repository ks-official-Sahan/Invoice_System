    private void loadRoles() {
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `roles`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                role.put(resultSet.getString("name"), resultSet.getString("id"));
            }
            roleComboBox.setModel(new DefaultComboBoxModel<>(vector));
        } catch (SQLException e) {
            DatabaseLogger.logger.log(Level.SEVERE, "role loading error: " + e.getMessage(), e.getMessage());
        }

    }

